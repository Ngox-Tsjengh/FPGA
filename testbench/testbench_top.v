`timescale 1ns/1ps
module testbench_top();
	
////////////////////////////////////////////////////////////
//参数定义

`define CLK_PERIORD		10		//时钟周期设置为10ns（100MHz）	

////////////////////////////////////////////////////////////
//接口申明
	
reg i_clk;
reg i_rst_n;


////////////////////////////////////////////////////////////	
//对被测试的设计进行例化
	
vlg_design		uut_vlg_design(
	.i_clk(i_clk),
	.i_rst_n(i_rst_n)
	
    );	
	
////////////////////////////////////////////////////////////
//复位和时钟产生

	//时钟和复位初始化、复位产生
initial begin
	i_clk <= 0;
	i_rst_n <= 0;
	#1000;
	i_rst_n <= 1;
end
	
	//时钟产生
always #(`CLK_PERIORD/2) i_clk = ~i_clk;	

////////////////////////////////////////////////////////////
//测试激励产生

initial begin

	@(posedge i_rst_n);	//等待复位完成
	
	@(posedge i_clk);
	

	repeat(300000) begin
		@(posedge i_clk);
	end
	
	$stop;
end


endmodule






